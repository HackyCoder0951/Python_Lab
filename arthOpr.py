print("Program of Arithmetic Operations :-")
print("Add/Sub/Mul/Div/Rem/Expo/Floor Div.")

n1 = float(input("Enter the first number :- "))

n2 = float(input("Enter the second number :- "))

#addition of two numbers
n3 = n1 + n2
print("Add of n1 + n2 :-",n3)

#substraction of two numbers
n3 = n1 - n2
print("Sub of n1 - n2 :-",n3)

#multiplication of two numbers
n3 = n1 * n2
print("Mul of n1 * n2 :-",n3)

#division of two numbers
n3 = n1 / n2
print("Div of n1 / n2 :-",n3)

#reminder of two numbers
n3 = n1 % n2
print("Rem of n1 % n2 :-",n3)

#exponent of two numbers
n3 = n1 ** n2
print("Exponent of n1 ** n2 :-",n3)

#floor division of two numbers
n3 = n1 // n2
print("Floor Div of n1 // n2 :-",n3)
